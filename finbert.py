def fetch_news_from_yahoo_rss(max_results: int = MAX_PER_SOURCE) -> list[dict]:
    """
    Yahoo Finance RSS — XML feed.
    Debug proof: prints HTTP status, raw XML item count, and first item title.
    """
    url = "https://finance.yahoo.com/news/rssindex"
    ts  = utc_now()

    try:
        resp = requests.get(url, timeout=10, headers={"User-Agent": "Mozilla/5.0"})
        resp.raise_for_status()

        root    = ET.fromstring(resp.content)
        channel = root.find("channel")
        items   = channel.findall("item") if channel is not None else []

        dprint(f"\n  [DEBUG][Yahoo] HTTP {resp.status_code} | fetched at {ts}")
        dprint(f"  [DEBUG][Yahoo] XML items found  : {len(items)}")
        if items:
            first_title = items[0].find("title")
            dprint(f"  [DEBUG][Yahoo] Sample title[0] : "
                   f"{first_title.text if first_title is not None else '—'}")

        headlines = []
        for item in items[:max_results]:
            t = item.find("title")
            lnk = item.find("link")
            if t is not None and t.text:
                headlines.append({
                    "headline"  : t.text.strip(),
                    "url"       : lnk.text.strip() if lnk is not None and lnk.text else "",
                    "source"    : "Yahoo",
                    "fetched_at": ts,
                })

        print(f"[Yahoo Finance]   Live headlines fetched : {len(headlines)}")
        return headlines

    except Exception as e:
        print(f"[Yahoo Finance]   FAILED ({e}) — using fallback data.")
        return [{"headline": h, "source": "Yahoo-fallback", "fetched_at": ts}
                for h in _fallback_yahoo()]


