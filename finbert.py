def get_sentiment(item: dict, finbert_pipeline) -> dict:
    """
    Classify one headline dict and return an enriched result dict.
    Preserves source, url, and fetched_at so the API can return them.
    """
    output = finbert_pipeline(item["headline"])[0]
    return {
        "headline"  : item["headline"],
        "label"     : output["label"].lower(),
        "score"     : round(output["score"], 4),
        "source"    : item.get("source", "unknown"),
        "url"       : item.get("url", ""),
        "fetched_at": item.get("fetched_at", "—"),
    }


