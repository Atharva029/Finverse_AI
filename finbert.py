def merge_and_deduplicate_items(
    *source_lists: list[dict],
    max_total: int = MAX_HEADLINES,
) -> list[dict]:
    """
    Merge all source lists, deduplicate by lowercase headline,
    and cap at max_total. Preserves source + timestamp metadata.
    """
    seen     = set()
    combined = []

    for source in source_lists:
        for item in source:
            key = item["headline"].strip().lower()
            if key and key not in seen:
                seen.add(key)
                combined.append(item)
            if len(combined) >= max_total:
                break
        if len(combined) >= max_total:
            break

    print(f"\n[Merge]           Unique finance headlines : {len(combined)}")
    return combined


