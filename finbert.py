def detect_category(headline: str) -> str | None:
    for cat, pattern in _CATEGORY_PATTERNS.items():
        if pattern.search(headline):
            return cat
    return None


def find_trending_category(results: list[dict]) -> str:
    counts: dict[str, int] = {}
    for r in results:
        cat = detect_category(r["headline"])
        if cat:
            counts[cat] = counts.get(cat, 0) + 1
    if not counts:
        return "General Market"
    dprint(f"\n  [DEBUG][Category] Full count breakdown: {counts}")
    return max(counts, key=counts.get)


