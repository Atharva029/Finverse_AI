def aggregate_sentiment(results: list[dict]) -> str:
    counts = {"positive": 0, "neutral": 0, "negative": 0}
    for r in results:
        counts[r.get("label", "neutral")] += 1

    total = sum(counts.values()) or 1

    print("\n[Sentiment Aggregation]")
    print(f"  {'Label':<12} {'Count':>5}   {'Share':>6}   Bar")
    print("  " + "-" * 46)
    for label, count in counts.items():
        pct = round(100 * count / total, 1)
        bar = "█" * max(1, int(pct / 5))
        print(f"  {label.capitalize():<12} {count:>5}   {pct:>5.1f}%   {bar}")

    return max(counts, key=counts.get)


