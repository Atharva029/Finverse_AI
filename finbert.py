def load_finbert_model():
    """
    Load ProsusAI/finbert from Hugging Face.
    In DEBUG_MODE, runs a 3-sentence self-test first so you can confirm
    the model is producing sensible outputs before processing real news.

    Self-test expected results:
        "Profits surged to record high"  → POSITIVE
        "Market closed unchanged today"  → NEUTRAL
        "Company files for bankruptcy"   → NEGATIVE
    """
    print(f"\n[FinBERT]  Loading '{FINBERT_MODEL}'...")
    t0 = time.time()

    tokenizer = AutoTokenizer.from_pretrained(FINBERT_MODEL)
    model     = AutoModelForSequenceClassification.from_pretrained(FINBERT_MODEL)
    fb        = pipeline(task="text-classification", model=model,
                         tokenizer=tokenizer, truncation=True, max_length=512)

    elapsed = round(time.time() - t0, 1)
    print(f"[FinBERT]  Model loaded in {elapsed}s.\n")

    # ── DEBUG: Self-test with 3 sentences of known expected sentiment ──────
    if DEBUG_MODE:
        print("  [DEBUG][FinBERT] Running self-test on 3 known sentences...")
        self_test_cases = [
            ("Company profits surged to a record high this quarter.",   "positive"),
            ("The market closed largely unchanged on Monday.",           "neutral"),
            ("The firm announced it is filing for bankruptcy.",          "negative"),
        ]
        all_passed = True
        for sentence, expected in self_test_cases:
            result = fb(sentence)[0]
            actual = result["label"].lower()
            score  = round(result["score"], 3)
            status = "✅ PASS" if actual == expected else "❌ FAIL"
            if actual != expected:
                all_passed = False
            print(f"    {status}  [{actual.upper():<8} {score:.3f}]  \"{sentence}\"")
        verdict = "✅ FinBERT is working correctly." if all_passed \
                  else "⚠️  Some labels differ — model may produce unexpected results."
        print(f"\n  [DEBUG][FinBERT] Self-test result: {verdict}\n")

    return fb


